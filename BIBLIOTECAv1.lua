scrollingFrame.Position = UDim2.new(0, 5, 0, 5)

scrollingFrame.BackgroundTransparency = 1

scrollingFrame.ScrollBarThickness = 6

scrollingFrame.Parent = frame

-- Crear un objeto GridLayout para organizar los botones en el ScrollingFrame

local gridLayout = Instance.new("UIGridLayout")

gridLayout.CellSize = UDim2.new(0, 120, 0, 30)

gridLayout.CellPadding = UDim2.new(0, 10, 0, 10)

gridLayout.HorizontalAlignment = Enum.HorizontalAlignment.Center

gridLayout.VerticalAlignment = Enum.VerticalAlignment.Top

gridLayout.SortOrder = Enum.SortOrder.LayoutOrder

gridLayout.Parent = scrollingFrame

-- Crear los botones en el ScrollingFrame

for i, opcion in ipairs(opciones) do

    local button = Instance.new("TextButton")

    button.Size = UDim2.new(1, 0, 1, 0)

    button.BackgroundColor3 = Color3.new(0, 0.7, 0)

    button.TextColor3 = Color3.new(1, 1, 1)

    button.Text = opcion.nombre

    button.LayoutOrder = i

    button.Parent = scrollingFrame

    -- Función para ejecutar el código de la opción seleccionada y cerrar el menú

    local function ejecutarCodigo()

        loadstring(opcion.codigo)()

