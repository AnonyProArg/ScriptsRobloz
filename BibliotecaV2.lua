local isMobile = system.getInfo("platform") == "android" or system.getInfo("platform") == "ios"

-- Función para simular eventos de puntero del mouse

local function simulateMousePointer(event)

    if isMobile then

        -- Obtener las coordenadas táctiles

        local touchX, touchY = event.x, event.y

        

        -- Simular eventos de puntero del mouse

        if event.phase == "began" then

            system.activate("mouse")

            system.setMouseFocus(true)

            system.moveMouse(touchX, touchY)

            

        elseif event.phase == "moved" then

            system.moveMouse(touchX, touchY)

            

        elseif event.phase == "ended" then

            system.activate("mouse")

            system.setMouseFocus(false)

        end

    end

    return true  -- Indica que el evento ha sido manejado correctamente

end

-- Manejo de eventos táctiles

local function handleTouchEvents()

    if isMobile then

        Runtime:addEventListener("touch", simulateMousePointer)

    end

end
